import * as React from 'react';
import PropTypes from 'prop-types';
import AppBar from '@mui/material/AppBar';
import Box from '@mui/material/Box';
import CssBaseline from '@mui/material/CssBaseline';
import Divider from '@mui/material/Divider';
import Drawer from '@mui/material/Drawer';
import IconButton from '@mui/material/IconButton';
import List from '@mui/material/List';
import ListItem from '@mui/material/ListItem';
import ListItemButton from '@mui/material/ListItemButton';
import ListItemText from '@mui/material/ListItemText';
import MenuIcon from '@mui/icons-material/Menu';
import Toolbar from '@mui/material/Toolbar';
import Typography from '@mui/material/Typography';
import Button from '@mui/material/Button';
// import { Log_out } from "../../../Redux-toolkit/Slice/authSlice";
import { removeToken } from "../../../../src/Redux-toolkit/Slice/authSlice";
import { useDispatch,useSelector } from "react-redux";
import { Link } from 'react-router-dom';
const drawerWidth = 240;
const navItems = [ 'Product', 'Create'];

function DrawerAppBar(props) {
  const data=useSelector((state)=>state.login)
  const { window } = props;
  const [mobileOpen, setMobileOpen] = React.useState(false);
  const dispatch = useDispatch();
  const handleDrawerToggle = () => {
    setMobileOpen((prevState) => !prevState);
  };

  // const logout=()=>{
  //        dispatch(Log_out())
  //      }
  const handleClick = () => {
    dispatch(removeToken());
  };
  const drawer = (
    <Box onClick={handleDrawerToggle} sx={{ textAlign: 'center' }}>
      <Typography variant="h6" sx={{ my: 2 }}>
        EDU-CARE
      </Typography>
      <Divider />
      <List>
      <ListItem  disablePadding  component={Link} to="/Home" sx={{justifyContent:"center"}}>Home</ListItem>
        {navItems.map((item) => (
          <ListItem key={item} disablePadding>
            <ListItemButton sx={{ textAlign: 'center' }}>
              <ListItemText primary={item} />
            </ListItemButton>
          </ListItem>
        ))}
        {data.isLogin === true?(<>
          <ListItem disablePadding>
            <ListItemButton sx={{ textAlign: 'center' }} component={Link} to={"/"} onClick={handleClick}>
              <ListItemText primary="Logout"/>
            </ListItemButton>
          </ListItem>

        </>):(<>
          <ListItem disablePadding>
            <ListItemButton sx={{ textAlign: 'center' }} component={Link} to={"/"}>
              <ListItemText primary="Login"/>
            </ListItemButton>
          </ListItem>
        </>)}
      </List>
    </Box>
  );

  const container = window !== undefined ? () => window().document.body : undefined;

  return (
    <Box sx={{ display: 'flex' }}>
      <CssBaseline />
      <AppBar component="nav">
        <Toolbar>
          <IconButton
            color="inherit"
            aria-label="open drawer"
            edge="start"
            onClick={handleDrawerToggle}
            sx={{ mr: 2, display: { sm: 'none' } }}
          >
            <MenuIcon />
          </IconButton>
          <Typography
            variant="h6"
            component="div"
            sx={{ flexGrow: 1, display: { xs: 'none', sm: 'block' } }}
          >
            EDU-CARE
          </Typography>
          <Box sx={{ display: { xs: 'none', sm: 'block' } }}>
          <Button  sx={{ color: '#fff' }} component={Link} to={"/Home"}>Home</Button>
         
            {navItems.map((item) => (
              <Button key={item} sx={{ color: '#fff' }} to={`/${item}`} component={Link}>
                {item}
              </Button>
            ))}
             {/* <Button onClick={logout} sx={{ color: "#fff" }}>
                Logout
              </Button> */}
 {data.isLogin ===true  ?(<>

              <Button sx={{ color: '#fff' }} component={Link} to="/" onClick={handleClick}>
                Logout
              </Button>
             </>):(<>
              <Button sx={{ color: '#fff' }} component={Link} to="/">
                Login
              </Button>
             </>)}
          </Box>
        </Toolbar>
      </AppBar>
      <nav>
        <Drawer
          container={container}
          variant="temporary"
          open={mobileOpen}
          onClose={handleDrawerToggle}
          ModalProps={{
            keepMounted: true, // Better open performance on mobile.
          }}
          sx={{
            display: { xs: 'block', sm: 'none' },
            '& .MuiDrawer-paper': { boxSizing: 'border-box', width: drawerWidth },
          }}
        >
          {drawer}
        </Drawer>
      </nav>
      <Box component="main" >
        <Toolbar />
      </Box>
    </Box>
  );
}

DrawerAppBar.propTypes = {
  /**
   * Injected by the documentation to work in an iframe.
   * You won't need it on your project.
   */
  window: PropTypes.func,
};

export default DrawerAppBar;
